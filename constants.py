# According to http://stackoverflow.com/q/201323/743730 it is not possible
# to create a regex to handle email addressees.  The following partial regex
# is from the answer at http://stackoverflow.com/a/14075810/743730
mail_regex = r"""(mailto:)?([-!#-'*+/-9=?A-Z^-~]+(\.[-!#-'*+/-9=?A-Z^-~]+)*|"([]!#-[^-~ \t]|(\\[\t -~]))+")@[0-9A-Za-z]([0-9A-Za-z-]{0,61}[0-9A-Za-z])?(\.[0-9A-Za-z]([0-9A-Za-z-]{0,61}[0-9A-Za-z])?)+"""
mail_compiled_regex = re.compile(mail_regex, re.IGNORECASE)


